    'Value', char(target.Tunables.PositionReferenceFeedforwardEnabled), ...
    'OutDataTypeStr', 'int32', ...
    'Position', [80 645 220 675]);
add_block('simulink/Sources/Constant', referencePlayRequestBlock, ...
    'Value', char(target.Tunables.ReferencePlayRequest), ...
    'OutDataTypeStr', 'int32', ...
    'Position', [80 695 220 725]);
add_block('simulink/Sources/Constant', homeToZeroRequestBlock, ...
    'Value', char(target.Tunables.HomeToZeroRequest), ...
    'OutDataTypeStr', 'int32', ...
    'Position', [80 745 220 775]);
add_block('simulink/Sources/Constant', homeToZeroSpeedBlock, ...
    'Value', char(target.Tunables.HomeToZeroSpeed), ...
    'OutDataTypeStr', 'int32', ...
    'Position', [80 795 220 825]);
add_block('simulink/Sources/Constant', referenceSampleTimeBlock, ...
    'Value', num2str(target.SampleTime), ...
    'OutDataTypeStr', 'double', ...
    'Position', [80 845 220 875]);
add_block('simulink/Sources/Constant', referencePositionUnitBlock, ...
    'Value', char(target.Tunables.PositionUnitMillimetersPerCount6064), ...
    'OutDataTypeStr', 'double', ...
    'Position', [80 895 220 925]);


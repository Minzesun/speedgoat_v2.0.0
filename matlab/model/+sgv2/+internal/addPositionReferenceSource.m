add_line(modelName, referencePositionUnitHandles.Outport, sourceHandles.Inport(8), 'autorouting', 'on');
add_line(modelName, controllerHandles.Outport(5), sourceHandles.Inport(9), 'autorouting', 'on');
add_line(modelName, positionActualHandles.Outport, sourceHandles.Inport(10), 'autorouting', 'on');

referenceBlocks = struct( ...
    'SourceBlock', sourceBlock, ...
    'PositionReferencePort', 1, ...
    'RateReferencePort', 2, ...
    'PositionCommandPort', 1, ...
    'RateCommandPort', 2);
end


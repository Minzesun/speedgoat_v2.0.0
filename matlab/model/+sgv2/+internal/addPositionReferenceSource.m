sourceHandles = get_param(sourceBlock, 'PortHandles');
positionHandles = get_param(positionValuesBlock, 'PortHandles');
rateHandles = get_param(rateValuesBlock, 'PortHandles');
feedforwardEnableHandles = get_param(feedforwardEnableBlock, 'PortHandles');
referencePlayRequestHandles = get_param(referencePlayRequestBlock, 'PortHandles');
homeToZeroRequestHandles = get_param(homeToZeroRequestBlock, 'PortHandles');
homeToZeroSpeedHandles = get_param(homeToZeroSpeedBlock, 'PortHandles');
referenceSampleTimeHandles = get_param(referenceSampleTimeBlock, 'PortHandles');
referencePositionUnitHandles = get_param(referencePositionUnitBlock, 'PortHandles');
controllerHandles = get_param(controllerBlock, 'PortHandles');
positionActualHandles = get_param(positionActualSourceBlock, 'PortHandles');

add_line(modelName, positionHandles.Outport, sourceHandles.Inport(1), 'autorouting', 'on');
add_line(modelName, rateHandles.Outport, sourceHandles.Inport(2), 'autorouting', 'on');
add_line(modelName, feedforwardEnableHandles.Outport, sourceHandles.Inport(3), 'autorouting', 'on');

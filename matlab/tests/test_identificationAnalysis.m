function tests = test_identificationAnalysis
tests = functiontests(localfunctions);
end

function testSummarizeIdentificationCaptureMetrics(testCase)
capture = struct( ...
    'Time', [0 0.002 0.004 0.006], ...
    'SpeedCommand60FF', int32([0 50 50 0]), ...
    'VelocityCommand60FF', int32([0 50 50 0]), ...
    'VelocityActual606C', int32([0 40 45 5]), ...
    'PositionActual6064', int32([1000 1005 1014 1016]), ...
    'ReadyToRun', uint8([1 1 1 1]), ...
    'Statusword6041', uint16([hex2dec('0027') hex2dec('0027') hex2dec('0027') hex2dec('0027')]), ...
    'ErrorCode603F', uint16([0 0 0 0]), ...
    'Metadata', struct( ...
        'IdentificationMaxSpeed60FF', int32(200), ...
        'IdentificationMaxTravel6064', int32(1000), ...
        'IdentificationStep6064', int32(100), ...
        'IdentificationStopBand6064', int32(20)));

summary = sgv2.analysis.summarizeIdentificationCapture(capture);

verifyEqual(testCase, summary.SampleTime, 0.002);
verifyEqual(testCase, summary.Duration, 0.006);
verifyEqual(testCase, summary.PositionDelta6064, int32([0 5 9 2])');
verifyEqual(testCase, summary.ApproxVelocityFromPosition6064, [0 2500 4500 1000]');
verifyEqual(testCase, summary.VelocityError606C, double([0 40 45 5]') - [0 2500 4500 1000]');
verifyEqual(testCase, summary.MaxAbsTravel6064, int32(16));
verifyEqual(testCase, summary.MaxAbsSpeedCommand60FF, int32(50));
verifyEqual(testCase, summary.ReadyFraction, 1);
verifyFalse(testCase, summary.FaultSeen);
verifyEqual(testCase, summary.Metadata.IdentificationMaxSpeed60FF, int32(200));
end

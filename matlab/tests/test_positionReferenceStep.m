function state = localInitialState()
state = struct( ...
    'SampleIndex', 1, ...
    'BasePosition6064', int32(0), ...
    'WasReady', false, ...
    'WasPlayRequested', false, ...
    'LastReference6064', int32(0), ...
    'HasReference', false);
end

function state = localStateFromResult(result)
state = struct( ...
    'SampleIndex', result.SampleIndexNext, ...
    'BasePosition6064', result.BasePosition6064Next, ...
    'WasReady', result.WasReadyNext, ...
    'WasPlayRequested', result.WasPlayRequestedNext, ...
    'LastReference6064', result.LastReference6064Next, ...
    'HasReference', result.HasReferenceNext);
end

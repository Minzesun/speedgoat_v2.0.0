function tests = test_positionReferenceStep
tests = functiontests(localfunctions);
end

function testReferenceWaitsForReadyAndLocksCurrentPosition(testCase)
positionValues = int32([0; 10; 20; 0]);
rateValues = int32([0; 5000; 5000; 0]);
state = localInitialState();

notReady = sgv2.control.computePositionReferenceStep( ...
    positionValues, rateValues, int32(1), uint8(0), int32(1234), ...
    int32(0), int32(0), int32(10), 0.002, 0.001, state);

verifyEqual(testCase, notReady.PositionReference6064, int32(1234));
verifyEqual(testCase, notReady.RateReference6064, int32(0));
verifyEqual(testCase, notReady.SampleIndexNext, 1);
verifyEqual(testCase, notReady.BasePosition6064Next, int32(1234));
verifyFalse(testCase, notReady.WasReadyNext);

parked = sgv2.control.computePositionReferenceStep( ...
    positionValues, rateValues, int32(1), uint8(1), int32(1234), ...
    int32(0), int32(0), int32(10), 0.002, 0.001, localStateFromResult(notReady));

verifyEqual(testCase, parked.PositionReference6064, int32(1234));
verifyEqual(testCase, parked.RateReference6064, int32(0));
verifyEqual(testCase, parked.SampleIndexNext, 1);
verifyEqual(testCase, parked.BasePosition6064Next, int32(1234));
verifyTrue(testCase, parked.WasReadyNext);
verifyFalse(testCase, parked.WasPlayRequestedNext);

play = sgv2.control.computePositionReferenceStep( ...
    positionValues, rateValues, int32(1), uint8(1), int32(1234), ...
    int32(1), int32(0), int32(10), 0.002, 0.001, localStateFromResult(parked));

verifyEqual(testCase, play.PositionReference6064, int32(1234));
verifyEqual(testCase, play.RateReference6064, int32(0));
verifyEqual(testCase, play.SampleIndexNext, 2);
verifyEqual(testCase, play.BasePosition6064Next, int32(1234));
verifyTrue(testCase, play.WasReadyNext);
verifyTrue(testCase, play.WasPlayRequestedNext);

next = sgv2.control.computePositionReferenceStep( ...
    positionValues, rateValues, int32(1), uint8(1), int32(1235), ...
    int32(1), int32(0), int32(10), 0.002, 0.001, localStateFromResult(play));

verifyEqual(testCase, next.PositionReference6064, int32(1244));
verifyEqual(testCase, next.RateReference6064, int32(5000));
verifyEqual(testCase, next.SampleIndexNext, 3);
end

